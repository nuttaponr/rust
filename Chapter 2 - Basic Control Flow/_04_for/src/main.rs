fn main() {
    //operator .. สร้าง iterator จากตัวเลขเริ่มต้นทางซ้ายไปเรื่อยๆจนถึง แต่ไม่รวมค่าทางด้านขวา
    for x in 0..5 {
        println!("{}", x);
    }

    //operator ..= สร้าง iterator จากตัวเลขเริ่มต้นทางซ้ายไปเรื่อยๆจนถึง และรวมค่าทางด้านขวา
    for x in 0..=5 {
        println!("{}", x);
    }
}
